class Product{
    constructor(menuId, name, photo, description, calories, price, starRatings, customerRatings){
        this.menuId = menuId;
        this.name = name;
        this.photo = photo;
        this.description = description;
        this.calories = calories;
        this.price = price;
        this.starRatings = starRatings;
        this.customerRatings = customerRatings;
    }
}

export default Product;